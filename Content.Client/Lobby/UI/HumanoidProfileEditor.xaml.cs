                        defaultSiliconVoice.ID,
                        defaultSiliconVoice.DefaultPitch,
                        defaultSiliconVoice.DefaultSpeed,
                        defaultSiliconVoice.DefaultPause,
                        defaultSiliconVoice.DefaultPolyphony,
                        defaultSiliconVoice.DefaultVolume
